# Run initial sync from repository root
cd ../../
echo -e "${YELLOW}ğŸ”„ Running initial sync...${NC}"
./manage.sh --sync --force

# Run initial build from repository root (stay at root level)
echo -e "${YELLOW}ğŸ”§ Running initial build...${NC}"
